package com.hi;

public class Ex11 {

	public static void main(String[] args) {
		
		// ********* java.lang  >>  Class Math
		
		// 절대값
		System.out.println(Math.abs(12));
		System.out.println(Math.abs(-12));
		
		double su = 3.54;
		System.out.println(Math.ceil(su)); // 4.0  // 올림
		System.out.println(Math.floor(su)); // 3.0 // 버림
		System.out.println(Math.round(su)); // 4   // 반올림

		System.out.println(Math.random());
	}

}

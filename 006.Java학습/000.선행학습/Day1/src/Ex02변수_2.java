
public class Ex02변수_2 {
	public static void main(String args[]) {
		int x = 100; // x에 100을 대입한다.
		double pi = 3.14;
		char ch = 'a';
		String str = "abc def";
		
		System.out.print("x = ");
		System.out.println(x);
		
		System.out.print("pi = ");
		System.out.println(pi);
		
		System.out.print("ch = ");
		System.out.println(ch);
		
		System.out.print("str = ");
		System.out.println(str);
		
	}
}


public class Ex03상수 {
	public static void main (String args[]) {
		final double PI = 3.14; // final로 선언하면 상수다. 값을 바꿀 수 없다.(저장공간 대문자로 선언)
		
		// PI = 3.141592; // 상수는 값의 변경이 불가.
		
		final char CH = 'A'; // 대문자로 선언한다.
		
		// 변수 : variable -> 메모리에 할당하고 쓴다. 데이터타입정해주고, 이름정해주면 된다.
		// 상수 : constant -> 한번 정해지면 변경 불가한 저장공간. (ex : PI, CH)
		//                   => 기존의 상수 > literal (ex : 3.14, A)
		//                      (숫자만 상수가 아니라 정해지면 상수(아스키코드 A=65이런식)
 		
		
	}
}

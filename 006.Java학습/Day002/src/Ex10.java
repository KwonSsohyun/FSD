class Ex10{
	public static void main(String[] args){
		// 변수 - 실수형
		int c;
		c = 1111;

		long d;
		d = 1111; // 1111은 int니까 사실 L안붙혀도 롱에 집어넣어도 문제는 없다.

		float a;
		a = 3.14f; // 공간만큼 확보 float 4byte

		double b; // 변수의 선언 // double 8byte
		b = 3.14; // 변수의 초기화

		System.out.println(a);
		System.out.println(b);
	}
}
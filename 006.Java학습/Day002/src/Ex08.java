class Ex08{
	public static void main(String[] args){
		int a; // 변수의 선언
		a = 12; // 변수의 초기화
		a = 21;
		System.out.println("a = " + a);

		// 10진수 정수형 (진짜 숫자형)
		byte b;
		short c;
		int d;
		long e;

		b = 127;
		c = 32767;
		d = 2147483647;
		e = 9223372036854775807L;

		System.out.println("byte :" + b);
		System.out.println("short :" + c);
		System.out.println("int :" + d);
		System.out.println("long :" + e);
	}
}